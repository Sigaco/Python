# Clasificación en secundaria #codedex

grado = int(input('Indica cuantos años tiene el alumno: (Entre 9 y 12 años):  '))

if grado == 9:
    print('Estás en el grado Freshman ')
elif grado == 10:
    print('Estás en el grado Sophomore ')
elif grado == 11:
    print('Estás en el grado Junior ')
elif grado == 12:
    print('Estás en el grado Senior ')
else:
    print('No estás en la secundaria ')